package com.chinasofti.pms.zmk.safety.entity;

public class Dengji {

	private Integer id;
	private String dengjiname;
	private String dengjimiaoshu;
	public Dengji(Integer id, String dengjiname, String dengjimiaoshu) {
		super();
		this.id = id;
		this.dengjiname = dengjiname;
		this.dengjimiaoshu = dengjimiaoshu;
	}
	public Dengji(String dengjiname, String dengjimiaoshu) {
		super();
		this.dengjiname = dengjiname;
		this.dengjimiaoshu = dengjimiaoshu;
	}
	public Dengji() {
		super();
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getDengjiname() {
		return dengjiname;
	}
	public void setDengjiname(String dengjiname) {
		this.dengjiname = dengjiname;
	}
	public String getDengjimiaoshu() {
		return dengjimiaoshu;
	}
	public void setDengjimiaoshu(String dengjimiaoshu) {
		this.dengjimiaoshu = dengjimiaoshu;
	}
	@Override
	public String toString() {
		return "Dengji [id=" + id + ", dengjiname=" + dengjiname + ", dengjimiaoshu=" + dengjimiaoshu + "]";
	}
	

}
