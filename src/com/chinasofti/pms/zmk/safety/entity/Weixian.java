package com.chinasofti.pms.zmk.safety.entity;

public class Weixian {

	private Integer wxid;
	private String yinsu;
	private String houguo;
	private String cuoshi;
	public Integer getWxid() {
		return wxid;
	}
	public void setWxid(Integer wxid) {
		this.wxid = wxid;
	}
	public String getYinsu() {
		return yinsu;
	}
	public void setYinsu(String yinsu) {
		this.yinsu = yinsu;
	}
	public String getHouguo() {
		return houguo;
	}
	public void setHouguo(String houguo) {
		this.houguo = houguo;
	}
	public String getCuoshi() {
		return cuoshi;
	}
	public void setCuoshi(String cuoshi) {
		this.cuoshi = cuoshi;
	}
	public Weixian(Integer wxid, String yinsu, String houguo, String cuoshi) {
		super();
		this.wxid = wxid;
		this.yinsu = yinsu;
		this.houguo = houguo;
		this.cuoshi = cuoshi;
	}
	public Weixian(String yinsu, String houguo, String cuoshi) {
		super();
		this.yinsu = yinsu;
		this.houguo = houguo;
		this.cuoshi = cuoshi;
	}
	public Weixian() {
		super();
	}
	@Override
	public String toString() {
		return "Weixian [wxid=" + wxid + ", yinsu=" + yinsu + ", houguo=" + houguo + ", cuoshi=" + cuoshi + "]";
	}
	
	
	

}
